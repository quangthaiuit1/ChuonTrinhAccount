package trong.lixco.com.entity;

import javax.persistence.Entity;

@Entity
public class PrivateConfig extends AbstractEntity {
	private String pattentDate;
	private String decimalSeparator;
	private String thousandSeparator;
	private String decimalPattent;
	private int decimalNumber;
	private boolean showHeader=true;
	public PrivateConfig(){
		this.pattentDate="dd/MM/yyyy";
		this.decimalSeparator=",";
		this.thousandSeparator=".";
		this.decimalPattent="###,###,###,###,###,###";
		this.decimalNumber=0;
	}
	public String getPattentDate() {
		return pattentDate;
	}
	public void setPattentDate(String pattentDate) {
		this.pattentDate = pattentDate;
	}
	public String getDecimalSeparator() {
		return decimalSeparator;
	}
	public void setDecimalSeparator(String decimalSeparator) {
		this.decimalSeparator = decimalSeparator;
	}
	public String getThousandSeparator() {
		return thousandSeparator;
	}
	public void setThousandSeparator(String thousandSeparator) {
		this.thousandSeparator = thousandSeparator;
	}
	public String getDecimalPattent() {
		return decimalPattent;
	}
	public void setDecimalPattent(String decimalPattent) {
		this.decimalPattent = decimalPattent;
	}
	public int getDecimalNumber() {
		return decimalNumber;
	}
	public void setDecimalNumber(int decimalNumber) {
		this.decimalNumber = decimalNumber;
	}
	public boolean isShowHeader() {
		return showHeader;
	}
	public void setShowHeader(boolean showHeader) {
		this.showHeader = showHeader;
	}
	
	
}
